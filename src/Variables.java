public class Variables {
    public static void main(String[] args) {
        double L;
        double S;
        double R;
        R = 1.0;
        L = 2 * Math.PI * R;
        S = Math.PI * Math.pow(R,2);
        System.out.println(L);
        System.out.println(S);
    }
}
